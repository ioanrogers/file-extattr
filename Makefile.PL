use ExtUtils::MakeMaker;

# Check whether we have <attr/attributes.h> and <attr/xattr.h> on Linux.
# Suggest what the user needs to install, to get these files.
@DIRS = qw(. /usr/include);
if ($^O eq 'linux')
{
    my %headers = (
        'attr/attributes.h' => 0,
        'attr/xattr.h' => 0,
    );
    my $incdir;

    foreach $incdir (@DIRS)
    {
        foreach (keys %headers)
        {
            $headers{$_}++ if (-r "$incdir/$_");
        }
    }

    foreach (keys %headers)
    {
        die "<$_> not found; perhaps you need to install libattr-devel"
            if ($headers{$_} == 0);
    }
}

# OpenBSD does not support extended attributes.
die 'OpenBSD does not support extended attributes' if ($^O eq 'openbsd');

